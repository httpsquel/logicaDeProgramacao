function quadDiferenca() {
    let a = parseInt(prompt("Insira o primeiro valor:"))
    let b = parseInt(prompt("Insira um segundo valor:"))

    let diferenca = b - a
    let resultado = diferenca * 2 

    alert("O quadrado da diferença dos dois valores é: " + resultado)
    return resultado
}

